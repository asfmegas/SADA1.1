    private double calcularNP1(){
        
        try{
            double nota1 = Double.parseDouble(jtNota1.getText());
            double nota2 = Double.parseDouble(jtNota2.getText());
            double nota3 = Double.parseDouble(jtNota3.getText());
            double media = (nota1+nota2+nota3)/3;
            return Double.valueOf(String.format(Locale.US, "%.2f", media));
        
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Erro fatal.\n"+e.getMessage());
            return 0;
        }
        
    }
    
