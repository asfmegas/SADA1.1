    private void moastrarPesquisa(List<Movimento> mov) {
        while(tmMovimento.getRowCount() > 0){
            tmMovimento.removeRow(0);
        }
        if(mov.isEmpty()){
            JOptionPane.showMessageDialog(null, "Sua lista est√° vazia!");
        }else{
            String[] linhas = new String[]{null,null,null,null,null,null,null,null,null};
            for(int i=0; i < mov.size();i++){
                tmMovimento.addRow(linhas);
                tmMovimento.setValueAt(mov.get(i).getNome(), i, 0);
                tmMovimento.setValueAt(mov.get(i).getTurma(), i, 1);
                tmMovimento.setValueAt(mov.get(i).getNota1(), i, 2);
                tmMovimento.setValueAt(mov.get(i).getNota2(), i, 3);
                tmMovimento.setValueAt(mov.get(i).getNota3(), i, 4);
                tmMovimento.setValueAt(mov.get(i).getNota3(), i, 5);
                tmMovimento.setValueAt(mov.get(i).getNota3(), i, 6);
                tmMovimento.setValueAt(mov.get(i).getNota3(), i, 7);
                tmMovimento.setValueAt(mov.get(i).getFaltas(), i, 8);
                tmMovimento.setValueAt(formatarDataCampo(mov.get(i).getDataAlteracao()), i, 9);
            }
        }
    }
    
