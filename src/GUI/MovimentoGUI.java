    private String getCampo(){
        String campo = "";
        if(jrAluno.isSelected()){
            campo = "a.nome";
        }else if(jrCurso.isSelected()){
            campo = "c.especificacao";
        }else if(jrDisc.isSelected()){
            campo = "d.especificacao";
        }else if (jrTurma.isSelected()){
            campo = "m.turma";
        }
        return campo;
    }
